#include "libft.h"

void	ft_striteri(char *s, void (*f)(unsigned int, char *))
{
	size_t	l;
	size_t	i;

	l = ft_strlen(s);
	i = 0;
	while (i < l)
	{
		f(i, s + i);
		i++;
	}
}
